-- Trigger Update Stock
CREATE OR REPLACE FUNCTION update_stock_on_purchase()
RETURNS TRIGGER AS $$
BEGIN
    -- Cek apakah sudah ada entry di "Stock" untuk produk ini
    IF NOT EXISTS (SELECT 1 FROM "Stock" WHERE "ProductId" = NEW."ProductId") THEN
        -- Jika belum ada, tambahkan entry baru dengan PurchaseQuantity
        INSERT INTO "Stock" ("ProductId", "StockQuantity")
        VALUES (NEW."ProductId", NEW."PurchaseQuantity");
    ELSE
        -- Jika sudah ada, update StockQuantity di tabel "Stock" berdasarkan PurchaseQuantity
        UPDATE "Stock"
        SET "StockQuantity" = "StockQuantity" + NEW."PurchaseQuantity"
        WHERE "ProductId" = NEW."ProductId";
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

