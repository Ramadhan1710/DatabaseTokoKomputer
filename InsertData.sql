-- Trigger Notice Stock Habis
CREATE OR REPLACE FUNCTION check_stock_and_notify()
RETURNS TRIGGER AS $$
DECLARE
    product_stock INTEGER;
BEGIN
    -- Mendapatkan jumlah stok produk setelah InvoiceLine diinsert
    SELECT "StockQuantity" INTO product_stock
    FROM "Stock"
    WHERE "ProductId" = NEW."ProductId";

    -- Memeriksa apakah stok hampir habis
    IF product_stock <= 2 THEN
        -- Gantilah pesan berikut sesuai kebutuhan Anda
        RAISE NOTICE 'Perhatian: Stok barang hampir habis untuk ProductId %!', NEW."ProductId";
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER stock_check_trigger
AFTER INSERT ON "InvoiceLine"
FOR EACH ROW
EXECUTE FUNCTION check_stock_and_notify();

-- Trigger Mengurangi Stock
CREATE OR REPLACE FUNCTION update_stock_on_invoice_line()
RETURNS TRIGGER AS $$
BEGIN
    -- Update StockQuantity di tabel "Stock" berdasarkan InvoiceQuantity
    UPDATE "Stock"
    SET "StockQuantity" = "StockQuantity" - NEW."InvoiceQuantity"
    WHERE "ProductId" = NEW."ProductId";

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Assign trigger ke tabel "InvoiceLine"
CREATE TRIGGER invoice_line_trigger
AFTER INSERT ON "InvoiceLine"
FOR EACH ROW
EXECUTE FUNCTION update_stock_on_invoice_line();

